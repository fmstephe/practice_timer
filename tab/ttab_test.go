package tab

import "fmt"

func ExampleCrossroadsCream() {
	motifStr := "[1:5,-,3:7,-,3:5,-,3:7,-,-,3:5,3:7,-,1:8,-,1:5]"
	motif := ParseMotif(motifStr + motifStr)
	fmt.Println(motif.String())
	// Output:
	//e||---------------|---------------||
	//B||---------------|---------------||
	//G||---------------|---------------||
	//D||--7-5-7--57----|--7-5-7--57----||
	//A||---------------|---------------||
	//E||5-----------8-5|5-----------8-5||
}
